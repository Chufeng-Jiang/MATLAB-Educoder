%%%1. 按要求创建矩阵A  %%%%
A=[1:3;4:6;7:9];


%%%2.分别对每行元素求和并存储到当前行的最后一列
A(1,3)=A(1,1)+A(1,2)+A(1,3);
A(2,3)=A(2,1)+A(2,2)+A(2,3);
A(3,3)=A(3,1)+A(3,2)+A(3,3);


%%%3. 用disp函数一字符串形式（num2str函数）输出A(3,3)的值%%%
disp(num2str(A(3,3)));


